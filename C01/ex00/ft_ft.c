#include <unistd.h>

void	ft_ft(int *nbr)
{
	*nbr = 4211;
}

int main()
{
	int i;
	char c[5];
	i = 0;
	ft_ft(&i);
	c[0] = (i / 1000) + '0';
	c[1] = ((i / 100) % 10) + '0';
	c[2] = ((i / 10) % 10) + '0';
	c[3] = (i % 10) + '0';
	c[4] = '0';
	write(1, c, 4);
}
